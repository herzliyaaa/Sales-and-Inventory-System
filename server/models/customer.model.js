const sql = require("../config/db.config");

// constructor
const Customer = function(customer) {
  this.firstname =  customer.firstname;
  this.middlename = customer.middlename;
  this.lastname = customer.lastname;
  this.customerAddress = customer.customerAddress;
  this.customerContact = customer.customerContact;
};


Customer.create = (newCustomer, result) => {
  sql.query("INSERT INTO customers SET ?", newCustomer, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    console.log("created customer: ", { id: res.insertId, ...newCustomer });
    result(null, { id: res.insertId, ...newCustomer });
  });
};

Customer.findById = (id, result) => {
  sql.query(`SELECT * FROM customers WHERE id = ${id}`, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(err, null);
      return;
    }

    if (res.length) {
      console.log("found customer: ", res[0]);
      result(null, res[0]);
      return;
    }

    // not found Customer with the id
    result({ kind: "not_found" }, null);
  });
};

Customer.getAll = (lastname, result) => {
  let query = "SELECT * FROM customers";

  if (lastname) {
    query += ` WHERE lastname LIKE '%${lastname}%'`;
  }

  sql.query(query, (err, res) => {
    if (err) {
      console.log("error: ", err);
      result(null, err);
      return;
    }

    console.log("customers: ", res);
    result(null, res);
  });
};

